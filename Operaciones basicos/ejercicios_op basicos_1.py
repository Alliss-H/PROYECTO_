# Ejercicios Operadores Basicos
num1 = 40
num2 = 16

# suma
suma= num1 + num2
print(num1,"+", num2, "=", suma)

# resta
resta= num1 - num2
print(num1,"-", num2, "=", resta)

# multiplicacion
multiplicacion= num1*num2
print(num1,"x", num2, "=", multiplicacion)

# division
division = num1/num2                        
print(num1,"/", num2, "=", division)

# division (entera)
divi_entera= num1//num2
print(num1,"//", num2, "=", divi_entera)

# modulo
mod= num1%num2
print(num1,"%", num2, "=", mod)

# potencia
potencia= num1**num2
print(num1,"*", num2, "=", potencia)
